package StaticDemo;

public class Box {
//    The word static means “belonging to the class, not to the object.”
//    static variable → shared by all objects.
//    static method → can be called without object, but can only use static stuff.
//    static block → runs once when class loads.
    static void greetings() {
        System.out.println("Greetings from Box!!");
    }
}
